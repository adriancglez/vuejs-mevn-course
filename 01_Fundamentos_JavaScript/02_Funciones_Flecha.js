// Para declarar una función de la forma normal
function myFunction() {
    // TODO: el código de la función.
}

// Para declarar una función de flecha
const myFunction = () => {
    // TODO: El código de la función
}

// Para declarar una función de flecha con parámetros
const myFunction = (param1, param2) =>{
    // TODO: El código de la función
}

// Si la función de flecha solo contendrá un parámetro, se pueden omitir los paréntesis
const myFunction = param1 => {
    // TODO: El código de la función
}

// Si la función solo contiene una expresión que retorna un valor
const myFunction = () => 'Hello World!';